package Training160423;

public class CyclesFor {
	
	public static void main(String[] args) {
		
		//пример шага цикла от 1 до 10
		for (int i = 1; i <= 10; i++){
			System.out.print(i + " ");
		}
		System.out.println();
		//пример обрантного отсчета шагов
		for (int i = 10; i >= 1; i--){
			System.out.print( + i + " ");
		}
		
		
		
	}

}
