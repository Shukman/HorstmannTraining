package Training160423;

public class CyclesFor1 {
	
	public static void main(String[] args) {
		System.out.print("Цикл For: ");
		for (int i = 10; i >= 1; i--){
			System.out.print( + i + " ");
		}
		
		System.out.print("\nЦикл While: ");
		int i = 10;
		while (i >= 1){ 
			System.out.print(i + " ");
			i--;
		}
		
		
		
	}

}
