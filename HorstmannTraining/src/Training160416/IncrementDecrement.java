package Training160416;

public class IncrementDecrement {
	
	public static void main(String[] args) {
		
		int i = 0;
		int j = 0;
		
		System.out.println(i++);
		System.out.println(++j);
		System.out.println("-------------");
		System.out.println(i);
		System.out.println(j);
		System.out.println("-------------");
		System.out.println(++i);
		System.out.println(j++);
		System.out.println("-------------");		
		System.out.println(i);
		System.out.println(j);
		System.out.println("-------------");
		System.out.println(i--);
		System.out.println(--j);
		System.out.println("-------------");
		System.out.println(i);
		System.out.println(j);
		System.out.println("-------------");
		System.out.println(--i);
		System.out.println(j--);
		System.out.println("-------------");		
		System.out.println(i);
		System.out.println(j);
		System.out.println("-------------");
		
		
	}

}
