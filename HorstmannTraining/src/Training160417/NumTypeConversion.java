package Training160417;

public class NumTypeConversion {
	
	public static void main(String[] args) {
		
		int n = 123456789;
		float f = n;
		System.out.println(f);
		
		
		int x = 1000;
		int y = 100000000;
		
		System.out.println(x * y);
		
		int x1 = 1000;
		long y1 = 100000000;
		
		System.out.println(x1 * y1);
		
		int x2 = 2;
		double y2 = 2.56;
		
		System.out.println(x2 * y2);
		
		
	}

}
